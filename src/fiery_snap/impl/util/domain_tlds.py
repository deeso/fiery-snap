FILE_EXTS = ['zip', 'html', 'exe']

FILE_EXTS_SET = set(FILE_EXTS)
FILTER_DOMAINS_SET = set(FILTER_DOMAINS)
TLDS_SET = set(TLDS)


def possible_domain(domain):
    if domain.replace('.', '').isdigit():
        return False 

    tld = domain.split('.')[-1] if domain.find('.') > 0 else None
    host = domain.split('.')[-2] if domain.find('.') > 0 else None

    if host is None or tld is None:
        return False

    if tld in FILE_EXTS_SET:
        return False

    return tld in TLDS_SET


def filter_domain(domain):
    if not possible_domain(domain):
        return True

    if len(domain.split('.')) > 2:
        t = ".".join(domain.split('.')[-2:])
        if t in FILTER_DOMAINS_SET:
            return True
    return domain in FILTER_DOMAINS_SET
